/*変数宣言の文 > 型 変数名;  例:int Kasan;

変数宣言に使用するデータ型

整数       格納可能な整数  利用頻度    浮動小数点型 float,double
---------------------------------   
byte   ----  ±約128         △      真偽値 boolean - trueかfalse
short  ----  ±約3.2万       △      
int    ----  ±約21億        ◎      文字 char - 一つの文字
long   ----  ±約900京       △      文字列 String - 文字の並び

整数を代入する場合、基本intを使う

小数を扱う場合、特別な理由が無い場合はdoubleを使用 ※真に厳密な計算ができないため、誤差が許されない計算では使用してはいけない

'定数の宣言の文 > final 型 定数名 = 初期値'
定数は値を書き換えできない*/